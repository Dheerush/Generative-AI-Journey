# Python Syntax
# Python Indentation : It means the spaces at the beginning of a code line. Python uses indentation to indicate a block of code. The standard is to use 4 spaces for indentation. It is important to be consistent with the number of spaces used for indentation in your code, as it can lead to syntax errors if not done correctly.
# Example of indentation in Python:
if 5 > 2 :
    print("5 is greater than 2") # notice the spaces before print statement which indicates that it is part of the if block. If we remove the spaces, it will give an error.
























